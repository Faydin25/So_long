#include "so_long.h"

void	check_ber(char *s)
{
	int	i;

	i = 0;
	if (!s)
		exit(0);
	while (s[i] != '\0')
		i++;
	if (s[i - 1] != 'r' && s[i - 2] != 'e'
		&& s[i - 3] != 'b' && s[i - 4] != '.')
	{
		ft_printf("Name is false");
		exit(0);
	}
}
